# -*- coding = utf-8 -*-
# 注释分为两类：单行注释和多行注释。
# 单行注释：只能注释一行内容，使用#符号。
# 多行注释：可以注释多行内容，使用左右各三个"""双引号和三个'''单引号包围。注意：多行注释不允许嵌套。
# 解释器不会解释任何注释内容。

# 演示-单行注释
# print("hello world")
print("hello world")  # 注释内容

# 演示-多行注释
'''
使用三个单引号进行多行注释:
print("hello python")
'''

"""
使用三个双引号进行多行注释：
print("hello world")
print("hello world")
"""
print("hello python")
